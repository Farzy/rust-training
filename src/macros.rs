use rust_training::helper;

// --------------------------------------------------------------------------------------------
// Recreate vec!
// --------------------------------------------------------------------------------------------

#[macro_export]
macro_rules! vec2 {
    ( $( $x:expr ),* ) => {
        {
            let mut temp_vec = Vec::new();
            $(
                temp_vec.push($x);
            )*
            temp_vec
        }
    };
}

// --------------------------------------------------------------------------------------------
// Main
// --------------------------------------------------------------------------------------------

pub fn main() {
    helper::subsection("macro_rules!");
    let v = vec2![1, 2, 3];

    println!("vec2 = {:?}", v);
}